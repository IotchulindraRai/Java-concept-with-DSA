package educatiio;

public class Switch {
public static void main(String[] args) {
	int v=5;
	 switch(v) {
	 case 1:
	 
		System.out.println("one");
		
	 case 2:
		System.out.println("two");
		break;
	 case 3:
		System.out.println("three");
			break;
		
	case 4: 
		System.out.println("four");
		break;
	case 5:
		System.out.println("five");
		
        break;
        default:
        	System.out.println("Nothing");
}
}
}